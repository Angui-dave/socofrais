class Product {
  final String name;
  final String description;
  final double price;
  final String image;
  final double? rating;
  final int? reviewCount;

  Product({
    required this.name,
    required this.description,
    required this.price,
    required this.image,
     this.rating,
     this.reviewCount,
  });
}





